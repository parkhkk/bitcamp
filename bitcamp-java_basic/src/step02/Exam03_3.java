// 변수 - 메모리에 값 저장하기

package step02;

public class Exam03_3 {
    public static void main(String[] args) {
        int a;
        // 할당 연산자(연산을 수행하는 명령)
        // - 오른쪽에 지정된 값을 왼쪽에 가리키는 이름의 메모리에
        //   저장을 시키는 명령어.
        a = 100;
        
        int b;
        b = a;

        System.out.println(a);
        System.out.println(b);
        
    }
}

