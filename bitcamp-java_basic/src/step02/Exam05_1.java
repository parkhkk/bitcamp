// 부동 소수점 변수 - 값 할당

package step02;

public class Exam05_1 {
    public static void main(String[] args) {
        //  유효자릿수 7자리 부동 소수점 저장 가능
        
        float f;
        f = 9.876545f;
        System.out.println(f);

        f = 987654.5f; 
        System.out.println(f);

        f = 0.000009876545f;
        System.out.println(f);

        f = 987654500;
        System.out.println(f);

    }
}

