// 정수의 리터럴(literal) 두번째 - 자릿수 표기

package step01;

public class Exam05_2 {
    public static void main(String[] args) {

        // _를 사용하여 읽기 쉽고록 숫자의 자릿수를 표시할 수 있다.
        // 자리 위치는 작성자 마음이다.
        System.out.println(12873406);
        System.out.println(1287_3406);
        System.out.println(12_873_406);

        //2진수
        System.out.println(0b1100100);
        System.out.println(0b110_0100);

    }
}


